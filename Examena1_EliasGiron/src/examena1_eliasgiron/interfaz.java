    private void jb_devolucionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_devolucionMouseClicked
        for (int i = 0; i < libros.size(); i++) {
            for (int j = 0; j < listausuarios.get(i).getLibros().size(); j++) {
                if (tf_devolucion.getText().equals(libros.get(i).getTitulo())) {
                    libros.add(listausuarios.get(i).getLibros().get(j));
                    listausuarios.get(i).getLibros().remove(j);
                } else {
                    JOptionPane.showMessageDialog(this, "El libro a devolver no esta en el inventario");
                }
            }

        }
    }//GEN-LAST:event_jb_devolucionMouseClicked

